let title = document.querySelector('h1')
let table = document.querySelector('.game__table')
let p = document.querySelector('.game__count')
let popup = document.querySelector('.overlay')
let questionBlock = document.querySelector('.question')
let questionText = document.querySelector('.question .popup__content')
let answerBlock = document.querySelector('.answer')
let answerText = document.querySelector('.answer .popup__content')
let yesBtn = document.querySelector('.popup__btn-yes')
let noBtn = document.querySelector('.popup__btn-no')
let okBtn = document.querySelector('.popup__btn-ok')
let next = document.querySelector('.next')
let round = 0
let count = 0

let play = Object.keys(data)[round]
let playRound = Object.values(data)[round]

next.addEventListener('click', function() {
    table.innerHTML = ''
    round++
    play = Object.keys(data)[round]
    playRound = Object.values(data)[round]
    getRound()
})

function getRound() {
    title.textContent = play
    for (let el in playRound) {
        let elem = playRound[el]
        let tr = document.createElement('tr')
        let th = document.createElement('th')
        th.textContent = el
        tr.append(th)

        for (let el of elem) {
            let price = el.price
            let question = el.question
            let image = el.image
            let answer = el.answer
            let td = document.createElement('td')
            td.textContent = price
            td.addEventListener('click', function func() {
                popup.style.display = 'block'
                questionBlock.classList.add('show')
                if (question != undefined) {
                    questionText.innerHTML = question
                    yesBtn.addEventListener('click', function() {
                        questionBlock.classList.remove('show')
                        answerBlock.classList.add('show')
                        questionText.innerHTML = ''
                        answerText.textContent = answer
                        count += price
                    })
                    noBtn.addEventListener('click', function() {
                        questionBlock.classList.remove('show')
                        answerBlock.classList.add('show')
                        questionText.innerHTML = ''
                        answerText.textContent = answer
                        count -= price
                    })
                    okBtn.addEventListener('click', function() {
                        popup.style.display = 'none'
                        answerBlock.classList.remove('show')
                        answerText.textContent = ''
                        price = 0
                        p.textContent = `Счет ${count}`
                        td.textContent = ''
                        td.removeEventListener('click', func)
                    })
                } else {
                    let img = document.createElement('img')
                    img.src = `image/${image}`
                    questionText.append(img)
                    yesBtn.addEventListener('click', function() {
                        questionBlock.classList.remove('show')
                        answerBlock.classList.add('show')
                        questionText.innerHTML = ''
                        answerText.textContent = answer
                        count += price
                    })
                    noBtn.addEventListener('click', function() {
                        questionBlock.classList.remove('show')
                        answerBlock.classList.add('show')
                        questionText.innerHTML = ''
                        answerText.textContent = answer
                        count -= price
                    })
                    okBtn.addEventListener('click', function() {
                        popup.style.display = 'none'
                        answerBlock.classList.remove('show')
                        answerText.textContent = ''
                        price = 0
                        p.textContent = `Счет ${count}`
                        td.textContent = ''
                        td.removeEventListener('click', func)
                    })
                }
            })
            tr.append(td)
        }

        table.append(tr) 
    }
}

